<script setup lang="ts">
import {
  type ColumnType,
  type LinkToAnotherRecordType,
  PermissionEntity,
  PermissionKey,
  type TableType,
  UITypes,
  isLinksOrLTAR,
  isVirtualCol,
} from 'nocodb-sdk'
import { fieldMatchesSearch, isBlankFieldValue } from './searchUtils'

const props = defineProps<{
  fields: ColumnType[]
  forceVerticalMode?: boolean
  isLoading: boolean
  showColCallback?: (col: ColumnType) => boolean
  isHiddenCol?: boolean
  /** Free-text query. fieldMatchesSearch normalizes internally so callers can pass raw input. */
  searchQuery?: string
  hideBlankFields?: boolean
  /** When true, render label + plain text instead of label + bordered input */
  compactMode?: boolean
}>()

// Cells whose editor paints a format hint (e.g. "YYYY-MM-DD") as its own
// placeholder when empty — skip '--' overlay for these so the two don't
// stack. Their *readonly* variant has no such hint (see
// cell/Date/Readonly.vue), so '--' is still shown when the cell is rendered
// read-only — see showCompactEmptyHint below.
const COMPACT_FORMAT_HINT_UIDTS = new Set<string>([UITypes.Date, UITypes.DateTime, UITypes.Time, UITypes.Year])

// Cells that always paint their own visible empty-state UI in both editable
// and readonly modes — '--' would stack on top, so always skip:
//   Attachment → upload prompt / file list
//   Rating     → zero filled stars
//   Checkbox   → unchecked indicator
//   Button     → button label / icon
const COMPACT_ALWAYS_SKIP_UIDTS = new Set<string>([UITypes.Attachment, UITypes.Rating, UITypes.Checkbox, UITypes.Button])

const { changedColumns, localOnlyChanges, isNew, loadRow: _loadRow, row: _row } = useExpandedFormStoreOrThrow()

const { isSqlView } = useSmartsheetStoreOrThrow()

const isPublic = inject(IsPublicInj, ref(false))

const meta = inject(MetaInj, ref())

const isTemplateMode = inject(IsTemplateModeInj, ref(false))

const blueprintParentTableId = inject(BlueprintParentTableIdInj, ref())

const parentBreadcrumbs = inject(TemplateBreadcrumbsInj, ref([]))

const { isUIAllowed } = useRoles()

const { isMobileMode } = useGlobal()

const { getMeta } = useMetas()

const { open: openExpandedFormDetached } = useExpandedFormDetached()

const readOnly = computed(() => !isUIAllowed('dataEdit') || isPublic.value || isSqlView.value)

const isSyncedColumn = (column: ColumnType) => meta.value?.synced && column?.readonly && !isAutoGeneratedColumn(column)

// In compact view, show '--' for an empty cell unless the cell already
// renders its own empty-state UI. Format-hint cells (date/time) only paint
// their hint in the editable variant — keep '--' when they mount read-only.
const showCompactEmptyHint = (col: ColumnType, isAllowed: boolean) => {
  if (!col.uidt) return true
  if (COMPACT_ALWAYS_SKIP_UIDTS.has(col.uidt)) return false
  if (!COMPACT_FORMAT_HINT_UIDTS.has(col.uidt)) return true
  return readOnly.value || !isAllowed || isSyncedColumn(col) || showReadonlyColumnTooltip(col)
}

/**
 * Check if an LTAR column points back to the parent table that opened this blueprint form.
 * Such columns are auto-linked and should be disabled (read-only) in the blueprint editor
 * to prevent circular linking.
 */
const isParentLtarColumn = (col: ColumnType): boolean => {
  if (!blueprintParentTableId.value || !isLinksOrLTAR(col)) return false
  const colOptions = col.colOptions as LinkToAnotherRecordType
  return colOptions?.fk_related_model_id === blueprintParentTableId.value
}

/**
 * Open a nested blueprint form for the given LTAR column.
 * When saved, the blueprint is added to the parent row's ltarState,
 * which will later be resolved into a real record when the template is used.
 */
const addBlueprintForColumn = async (col: ColumnType) => {
  const colOptions = col.colOptions as LinkToAnotherRecordType
  const relatedTableId = colOptions?.fk_related_model_id
  const baseId = meta.value?.base_id
  if (!relatedTableId || !baseId) return

  try {
    const relatedMeta = (await getMeta(baseId, relatedTableId)) as TableType
    if (!relatedMeta) return

    // Build breadcrumb trail: append current table name to parent trail
    const breadcrumbs = [...parentBreadcrumbs.value, meta.value?.title || '']

    openExpandedFormDetached({
      isOpen: true,
      row: { row: {}, oldRow: {}, rowMeta: { new: true } },
      meta: relatedMeta,
      loadRow: false,
      useMetaFields: true,
      blueprintMode: true,
      blueprintParentTableId: meta.value?.id,
      breadcrumbs,
      newRecordSubmitBtnText: 'Save Record',
      newRecordHeader: `New ${relatedMeta.title} Record`,
      createdRecord: (record: Record<string, any>) => {
        const blueprint = { ...record, _isBlueprint: true }
        // Ensure ltarState structure exists
        if (!_row.value.rowMeta) _row.value.rowMeta = {}
        if (!_row.value.rowMeta.ltarState) _row.value.rowMeta.ltarState = {}

        if (isHm(col) || isMm(col)) {
          if (!_row.value.rowMeta.ltarState[col.title!]) {
            _row.value.rowMeta.ltarState[col.title!] = []
          }
          _row.value.rowMeta.ltarState[col.title!].push(blueprint)
        } else {
          // BT or OO — single linked record
          _row.value.rowMeta.ltarState[col.title!] = blueprint
        }
      },
    })
  } catch (e) {
    console.error('Failed to open blueprint form:', e)
  }
}

const getRelatedTableName = (col: ColumnType): string => {
  const colOptions = col.colOptions as LinkToAnotherRecordType
  const relatedTableId = colOptions?.fk_related_model_id
  const baseId = meta.value?.base_id
  if (!relatedTableId || !baseId) return 'Sub Record'
  const { getMetaByKey } = useMetas()
  const relatedMeta = getMetaByKey(baseId, relatedTableId)
  return relatedMeta?.title || 'Sub Record'
}

const showCol = (col: ColumnType) => {
  const baseVisible = props.showColCallback?.(col) || !isVirtualCol(col) || !isNew.value || isLinksOrLTAR(col)
  if (!baseVisible) return false
  if (!fieldMatchesSearch(col, props.searchQuery ?? '', _row.value?.row)) return false
  if (props.hideBlankFields && col.title && isBlankFieldValue(_row.value?.row?.[col.title], col)) return false
  return true
}

const revertLocalOnlyChanges = (col: string) => {
  if (localOnlyChanges.value[col]) {
    _row.value.row[col] = localOnlyChanges.value[col]
    changedColumns.value.delete(col)
    triggerRef(changedColumns)
    delete localOnlyChanges.value[col]
  }
}

function onCellValueChange(colTitle: string | undefined) {
  if (!colTitle) return
  changedColumns.value.add(colTitle)
  triggerRef(changedColumns)
}
</script>

<template>
  <div
    v-for="col of fields"
    v-show="showCol(col)"
    :key="col.title"
    :class="[`nc-expand-col-${col.title}`, { 'nc-row-compact': compactMode }]"
    :col-id="col.id"
    :data-testid="`nc-expand-col-${col.title}`"
    class="nc-expanded-form-row w-full"
  >
    <div
      class="flex items-start nc-expanded-cell min-h-[32px]"
